                        <li><b>Schwarzbier</b></li>
                        <li><b>Bock</b></li>
                        <li><b>American Pale Lager</b></li>
                        <li><b>European Pale Lager</b></li>
                        <li><b>Kolsch</b></li>
                    </ul>
            </p></li><br>
            <li><p><b>Ale:</b> Estas, al contrario que las lager,<strong>son de fermentación alta</strong>. Estas fueron ganando más popularidad que las otras, sin embargo, esto depende mucho del gusto personal. Al igual que las Lager, de estas también se desprenden varios grupos grandes (Entre ellas, las <b>Stout y Porter, Pale Ale, German Ale</b>).<br>
            
