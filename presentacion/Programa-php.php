<h1> Programa para control de Stock</h1>
<br>
<p class="programaStock"> Este programa partio de una necesidad al comenzar un emprendimiento de delivery de cervezas que empezamos junto a mi hermano. Si bien, aun no esta terminado y hay mucho que mejorar (Lo cual, voy realizando mejoras de a poco) cumple con las funciones basicas que necesitabamos: </p><br>

    <ul class="programaPhp">
        <li> Llevar un control del stock de los productos, junto a precios para la venta y costos.</li>
        <li> Guardado de todas las ventas realizadas con una vista amigable.</li>
        <li> Sistema de "carrito", donde una venta pasa por 3 estados: proceso, pendiente y finalizado. El cual nos ayuda a organizar las entregas.</li>
        <li> Pensado principalmente para mobile, lo cual lo podemos usar desde el celular facilmente cuando estemos en la calle.</li>
    </ul>


